                <p>In November 2012, the Enforcement Directorate began investigating Flipkart for alleged violations of foreign direct investment regulations of the Foreign Exchange Management Act, 1999.[97][98] On 30 November 2012, Flipkart's offices were raided by the Enforcement Directorate. Documents and computer hard drives were seized by the regulatory agency.[99]

In August 2014, the Enforcement Directorate claimed that it had found Flipkart to be in violation of the Foreign Exchange Management Act.[100]

The Delhi High Court observed violation of foreign investment regulations by E-Commerce firms including Flipkart.[101]

In January 2016, a public interest litigation came up for hearing which accused Flipkart of contravention of foreign investment norms. The court asked the Reserve Bank of India to provide the latest circular on foreign investment policy.[102]

In January 2016, the Department of Industrial Policy and Promotion (DIPP) clarified that it does not recognise the marketplace model of online retail.[103]

In February 2016, Health Minister, J P Nadda, informed that the Maharashtra FDA had taken action against Flipkart, among others, for selling drugs without valid license.</p>
